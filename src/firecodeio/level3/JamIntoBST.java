//Oracle
public class JamIntoBST {

    class TreeNode {

        int data;
        TreeNode left;
        TreeNode right;

        TreeNode() {
        }

        TreeNode(int data) {
            this.data = data;
        }

        TreeNode(int data, TreeNode left, TreeNode right) {
            this.data = data;
            this.left = left;
            this.right = right;
        }
    }

    public TreeNode insert(TreeNode root, int data) {
        if (root == null) {
            TreeNode nd = new TreeNode(data);
            return nd;
        } else {
            if (data < root.data) {
                root.left = insert(root.left, data);
            } else if (data > root.data) {
                root.right = insert(root.right, data);
            }
        }
        return root;
    }
}
